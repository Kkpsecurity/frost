                            <div class="lead mb-4 text-muted">
                                Florida's gun laws have undergone significant changes in recent years. This comprehensive guide breaks down everything gun owners, security professionals, and concealed carry permit holders need to know to stay compliant in 2025.
                            </div>

                            <h2 class="section-title">Constitutional Carry: What Changed in 2023</h2>
                            <p>
                                On July 1, 2023, Florida became the 26th state to adopt constitutional carry, also known as permitless carry. This landmark legislation allows eligible adults to carry concealed firearms without a permit. However, this doesn't mean all regulations have disappeared.
                            </p>

                            <div class="info-box mb-4">
                                <h4><i class="fas fa-info-circle text-info me-2"></i>Key Changes:</h4>
                                <ul>
                                    <li>No permit required for concealed carry for eligible residents</li>
                                    <li>Must be 21 or older (with military exceptions for 18+)</li>
                                    <li>Federal background check still required for purchases</li>
                                    <li>All existing prohibited locations remain in effect</li>
                                </ul>
                            </div>

                            <h2 class="section-title">Who Can Carry Under Florida Law?</h2>
                            <p>
                                While constitutional carry expanded gun rights, not everyone is eligible. Florida law still maintains strict requirements:
                            </p>

                            <h3>Eligibility Requirements:</h3>
                            <ul class="requirement-list">
                                <li><strong>Age:</strong> 21 years or older (18+ for active military)</li>
                                <li><strong>Legal Status:</strong> U.S. citizen or legal resident</li>
                                <li><strong>Background:</strong> Pass federal background check</li>
                                <li><strong>Mental Health:</strong> No disqualifying mental health adjudications</li>
                                <li><strong>Criminal History:</strong> No felony convictions or certain misdemeanors</li>
                            </ul>

                            <div class="warning-box mb-4">
                                <h4><i class="fas fa-exclamation-triangle text-warning me-2"></i>Important Note:</h4>
                                <p>Constitutional carry only applies to concealed carry. Open carry is still prohibited in most public places except for specific activities like hunting, fishing, or traveling to/from such activities.</p>
                            </div>

                            <h2 class="section-title">Prohibited Locations</h2>
                            <p>
                                Even with constitutional carry, certain locations remain strictly off-limits for firearms:
                            </p>

                            <div class="row">
                                <div class="col-md-6">
                                    <h4>Government Buildings</h4>
                                    <ul>
                                        <li>Courthouses and court facilities</li>
                                        <li>Police stations</li>
                                        <li>Government meetings</li>
                                        <li>Airport secure areas</li>
                                    </ul>
                                </div>
                                <div class="col-md-6">
                                    <h4>Educational Institutions</h4>
                                    <ul>
                                        <li>K-12 school property</li>
                                        <li>School buses</li>
                                        <li>Career centers</li>
                                        <li>College/university facilities*</li>
                                    </ul>
                                    <small class="text-muted">*Some exceptions for permitted individuals</small>
                                </div>
                            </div>

                            <div class="row mt-4">
                                <div class="col-md-6">
                                    <h4>Entertainment Venues</h4>
                                    <ul>
                                        <li>Bars and nightclubs</li>
                                        <li>Portions of restaurants serving alcohol</li>
                                        <li>Professional sporting events</li>
                                        <li>Certain entertainment facilities</li>
                                    </ul>
                                </div>
                                <div class="col-md-6">
                                    <h4>Other Restricted Areas</h4>
                                    <ul>
                                        <li>Correctional facilities</li>
                                        <li>Mental health facilities</li>
                                        <li>Certain meetings and events</li>
                                        <li>Private property when posted</li>
                                    </ul>
                                </div>
                            </div>

                            <h2 class="section-title">Benefits of Maintaining a Concealed Carry Permit</h2>
                            <p>
                                While a permit isn't required for concealed carry, there are significant advantages to maintaining one:
                            </p>

                            <div class="benefits-grid">
                                <div class="benefit-card">
                                    <i class="fas fa-map-marked-alt text-info"></i>
                                    <h4>Reciprocity</h4>
                                    <p>Travel to other states that recognize Florida permits</p>
                                </div>
                                <div class="benefit-card">
                                    <i class="fas fa-shopping-cart text-info"></i>
                                    <h4>Purchasing</h4>
                                    <p>Skip background checks for firearm purchases</p>
                                </div>
                                <div class="benefit-card">
                                    <i class="fas fa-shield-alt text-info"></i>
                                    <h4>Legal Protection</h4>
                                    <p>Additional legal protections in certain situations</p>
                                </div>
                                <div class="benefit-card">
                                    <i class="fas fa-school text-info"></i>
                                    <p>Access to certain restricted areas with proper authorization</p>
                                </div>
                            </div>

                            <h2 class="section-title">Purchase Requirements</h2>
                            <p>
                                Firearm purchases still require federal background checks and compliance with both state and federal laws:
                            </p>

                            <h3>For Handguns:</h3>
                            <ul>
                                <li>Must be 21 or older</li>
                                <li>Valid government-issued ID</li>
                                <li>Pass NICS background check</li>
                                <li>3-day waiting period (with exceptions)</li>
                                <li>Florida residents must provide proof of residency</li>
                            </ul>

                            <h3>Waiting Period Exceptions:</h3>
                            <ul>
                                <li>Valid concealed carry permit holders</li>
                                <li>Law enforcement officers</li>
                                <li>Military personnel</li>
                                <li>Certain hunters during hunting season</li>
                            </ul>

                            <h2 class="section-title">Red Flag Laws and Due Process</h2>
                            <p>
                                Florida enacted a Risk Protection Order (RPO) law following the 2018 Parkland shooting. Understanding this law is crucial for gun owners:
                            </p>

                            <div class="process-steps">
                                <div class="step">
                                    <div class="step-number">1</div>
                                    <div class="step-content">
                                        <h4>Petition Filed</h4>
                                        <p>Law enforcement can petition for temporary removal of firearms</p>
                                    </div>
                                </div>
                                <div class="step">
                                    <div class="step-number">2</div>
                                    <div class="step-content">
                                        <h4>Initial Hearing</h4>
                                        <p>Court reviews petition and may issue temporary order</p>
                                    </div>
                                </div>
                                <div class="step">
                                    <div class="step-number">3</div>
                                    <div class="step-content">
                                        <h4>Full Hearing</h4>
                                        <p>Gun owner can contest the order with legal representation</p>
                                    </div>
                                </div>
                                <div class="step">
                                    <div class="step-number">4</div>
                                    <div class="step-content">
                                        <h4>Final Decision</h4>
                                        <p>Court decides on final risk protection order</p>
                                    </div>
                                </div>
                            </div>

                            <h2 class="section-title">Transportation Laws</h2>
                            <p>
                                Florida has specific requirements for transporting firearms:
                            </p>

                            <h3>In Vehicles:</h3>
                            <ul>
                                <li><strong>Concealed carry permit holders:</strong> May carry loaded and accessible</li>
                                <li><strong>Non-permit holders:</strong> Firearm must be securely encased or not readily accessible</li>
                                <li><strong>Long guns:</strong> May be carried in vehicles for lawful purposes</li>
                                <li><strong>Ammunition:</strong> May be stored separately for maximum safety</li>
                            </ul>

                            <h2 class="section-title">Self-Defense and Stand Your Ground</h2>
                            <p>
                                Florida's Stand Your Ground law provides important protections but comes with responsibilities:
                            </p>

                            <div class="law-summary">
                                <h4>Key Provisions:</h4>
                                <ul>
                                    <li>No duty to retreat if you reasonably believe force is necessary</li>
                                    <li>Must reasonably believe threat is imminent</li>
                                    <li>Force used must be proportional to threat</li>
                                    <li>Immunity from criminal prosecution and civil action if justified</li>
                                </ul>
                            </div>

                            <div class="warning-box">
                                <h4><i class="fas fa-gavel text-warning me-2"></i>Legal Advice:</h4>
                                <p>Always consult with a qualified attorney familiar with Florida gun laws. Self-defense situations are fact-specific and complex.</p>
                            </div>

                            <h2 class="section-title">Recent Legislative Changes</h2>
                            <p>
                                Stay informed about recent changes that affect gun owners:
                            </p>

                            <h3>2025 Updates:</h3>
                            <ul>
                                <li>Enhanced penalties for gun crimes in certain zones</li>
                                <li>Updated reciprocity agreements with other states</li>
                                <li>Revised training requirements for certain permit applications</li>
                                <li>New regulations for gun storage around minors</li>
                            </ul>

