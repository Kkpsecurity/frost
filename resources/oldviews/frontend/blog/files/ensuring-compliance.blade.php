<h1>Ensuring Compliance with Online Security Training Standards</h1>

<p>With the increasing demand for online security training programs, it is essential to adhere to the strict guidelines
    and regulations set forth by governing bodies. One such set of regulations is outlined in the 5N-1.132 Firearms
    Training rules. These rules establish the requirements for initial firearms qualification, annual requalification
    training, firearms instruction, instructor recordkeeping, and online training standards. This article will explore
    how the STG platform aligns with these regulations, providing a comprehensive and secure online environment for
    firearms training.</p>

<h2>1. Initial Firearms Qualification:</h2>
<p>The STG platform offers a robust training program that satisfies the 28-hour range and classroom training
    requirement. Through live online instruction facilitated by Class "K" firearms instructors, participants can
    successfully complete the training while ensuring their identity, attendance, and successful completion are
    verified.</p>
<p>The STG platform goes beyond providing online instruction for firearms training. It also offers a comprehensive
    solution for students to register for the crucial 8 hours of in-person range training, which focuses on safely
    handling and storing firearms. This range of training is essential for individuals seeking initial qualification and
    is conducted in collaboration with STG's affiliate Class "K" instructor partners throughout the state.</p>
<p>By incorporating the in-person range training into our program, STG ensures that students have access to a complete
    and well-rounded firearms training experience. This aspect of the training allows individuals to gain hands-on
    experience and practical skills under the guidance of qualified instructors.</p>
<p>STG's commitment to meeting the state requirements for the statewide firearms license is reflected in the design of
    our robust firearms training program. By combining online instruction and in-person range training, we provide
    students with a comprehensive training curriculum that covers all necessary aspects of firearm safety, handling, and
    storage.</p>
<p>Our platform enables students statewide to conveniently access and complete the STG firearms training program,
    ensuring they meet the state's rigorous standards. With our network of affiliate Class "K" instructor partners, we
    can offer training opportunities across different locations, making it easier for individuals to find a suitable
    training facility nearby.</p>
<p>STG aims to equip students with the knowledge, skills, and confidence to safely and responsibly handle firearms. By
    providing a seamless online learning experience and facilitating in-person range training, we strive to ensure that
    our students receive the highest quality firearms education while complying with the state's requirements for
    obtaining a statewide firearms license.</p>

<h2>2. Annual Firearms Reporting Requirement:</h2>
<p>To meet the annual requalification training requirement, the STG platform allows participants to submit the required
    Certificate of Firearms Proficiency for Statewide Firearms License. This submission can be made online by the state
    by the K instructor, ensuring a streamlined process and eliminating any delays in reporting. By complying with the
    submission deadline specified on the license, licensees can maintain their qualifications seamlessly.</p>

<h2>3. Firearms Instruction:</h2>
<p>The STG platform provides access to the Firearms Training Manual Student Handbook and Study Guide, which complies
    with the instruction material requirements for both initial qualification and annual requalification training. Class
    "G" applicants and licensees can access this resource online, facilitating a comprehensive understanding of the
    training material. Additionally, the platform supports the Firearms Training Manual Instructor's Guide, assisting
    Class "K" licensed firearms instructors in meeting the instructional requirements.</p>

<h2>4. Firearms Instructor Recordkeeping Requirements:</h2>
<p>With the STG platform's robust recordkeeping features, instructors can easily maintain accurate records of all
    training sessions. These records include the instructor's name, license number, and compliance with security
    protocols. The platform ensures immediate accessibility of records to department investigators, contributing to a
    transparent and accountable training process.</p>

<h2>5. Online Firearm Classroom Training:</h2>
<p>The STG platform fully supports the requirements for online training outlined in the 5N-1.132 regulations. The
    platform guarantees a secure online environment with live transmission format, security protocols, and SSL/TLS
    technology. Student identity verification using U.S. state or federal-issued photo identification is integrated into
    the platform, ensuring the integrity of the training process. The digital log feature documents student attendance,
    while the limitation of log-in to a single device ensures a controlled learning experience.</p>

<h2>6. Instructor Requirements for Online Courses:</h2>
<p>The STG platform caters to the recordkeeping requirements for instructors conducting online courses. Detailed records
    of training sessions, instructor information, and compliance with security protocols are easily maintained within
    the platform. Instructors can ensure transparency and cooperation with department investigators by providing
    immediate access to records.</p>

