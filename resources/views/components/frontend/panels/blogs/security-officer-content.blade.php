            <ul class="text-light">
                <li>Passing a score requires answering at least 128 questions correctly, with no more than 50% being true or false.</li>
                <li>Issuance of Certificate of Security Officer Training within 3 business days using Form FDACS-16103.</li>
                <li>Training programs approved by the Florida Criminal Justice Standards and Training Commission are deemed approved by the department.</li>
            </ul>
