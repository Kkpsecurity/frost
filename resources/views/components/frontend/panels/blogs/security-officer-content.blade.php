            <h3 class="text-frost-primary h5">Initial Firearms Qualification</h3>
            <ul class="text-light">
                <li>Completion of 28 hours of range and classroom training by Class "K" firearms instructor(s)</li>
                <li>Classroom training options: in-person or live online instruction with verification of identity and attendance</li>
                <li>8 hours of in-person range training covering safe handling and storage of firearms</li>
                <li>The qualification allows using specific revolver or semiautomatic handgun calibers for regulated duties</li>
                <li>Licensees can only carry firearms they have completed training with</li>
                <li>Submission of the original white page from Certificate of Firearms Proficiency for Statewide Firearms License with the license application</li>
            </ul>
