            <div class="row">
                <div class="col-md-6">
                    <ul class="text-light">
                        <li>Live transmission format with Class "DI" instructors and students participating simultaneously</li>
                        <li>Online instruction is conducted from a school or training facility in Florida</li>
                        <li>Live access is provided to department investigators for auditing, monitoring, or inspection</li>
                        <li>Online instruction and testing through a secure website using SSL or TLS technology</li>
                        <li>Verification of student identity using U.S. state or federal-issued photo identification</li>
                        <li>Verification and documentation of daily attendance in a digital log</li>
                        <li>Restriction of student log-in to a single device</li>
                    </ul>
                </div>
                <div class="col-md-6">
                    <ul class="text-light">
                        <li>Inclusion of security questions to ensure active student participation</li>
                        <li>The minimum time requirement for reading screens with text</li>
                        <li>Opportunity for students to submit questions to Class "DI" instructors</li>
                        <li>Delivery of missed class hours through recorded instruction for absent students</li>
                        <li>Administration of online tests with randomized questions</li>
                        <li>Verification of completion of online training before reporting to the division</li>
                    </ul>
                </div>
