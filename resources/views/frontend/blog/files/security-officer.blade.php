<h1>5N-1.140 Security Officer, Recovery Agent, and Private Investigative Intern School Curriculum</h1>

<h2>Security Officer Schools and Training Facilities</h2>
<ul>
    <li>Passing a score requires answering at least 128 questions correctly, with no more than 50% being true or false.
    </li>
    <li>Issuance of Certificate of Security Officer Training within 3 business days using Form FDACS-16103.</li>
    <li>Training programs approved by the Florida Criminal Justice Standards and Training Commission are deemed approved
        by the department.</li>
</ul>

<h1>Online Training Requirements:</h1>
<ul>
    <li>Live transmission format with Class "DI" instructors and students participating simultaneously.</li>
    <li>Online instruction is conducted from a school or training facility in Florida.</li>
    <li>Live access is provided to department investigators for auditing, monitoring, or inspection.</li>
    <li>Online instruction and testing through a secure website using SSL or TLS technology.</li>
    <li>Verification of student identity using U.S. state or federal-issued photo identification.</li>
    <li>Verification and documentation of daily attendance in a digital log.</li>
    <li>Restriction of student log-in to a single device.</li>
    <li>Inclusion of security questions to ensure active student participation.</li>
    <li>The minimum time requirement for reading screens with text.</li>
    <li>Opportunity for students to submit questions to Class "DI" instructors.</li>
    <li>Delivery of missed class hours through recorded instruction for absent students.</li>
    <li>Administration of online tests with randomized questions.</li>
    <li>Verification of completion of online training before reporting to the division.</li>
</ul>


<h1>Retention of Records</h1>

<h2>Records to be maintained by schools or facilities administering examinations:</h2>
<ol>
    <li>Schedule including date, time, location, and instructor of each class session.</li>
    <li>Separate files for each course containing course materials, reference sources, and graded final exams.</li>
    <li>Log for each class session with student signatures.</li>
    <li>Copy of the Certificate of Security Officer Training presented to each student.</li>
    <li>Separate file for each approved instructor with qualifications and license copy.</li>
    <li>Records are to be maintained for a minimum of two years and produced upon request.</li>
</ol>

<h2>Additional record-keeping requirements for Class "DS" security schools or training facilities offering online
    courses:</h2>
<ul>
    <li>A digital record of student attendance log, records of training sessions, and proof of compliance with security
        protocols.</li>
    <li>Accessibility of records to department investigators upon request.</li>
    <li>The explanation for the unavailability of electronic records, with provision within 3 business days.</li>
</ul>

<h2>Options for meeting division record-keeping requirements:</h2>
<ul>
    <li>Retention of original paper records or scanning into electronic format.</li>
    <li>Electronic retention of online training records, reproducible or transmittable upon request.</li>
</ul>

<h1>Outline: 5N-1.132 Firearms Training</h1>

<h2>Initial Firearms Qualification</h2>
<ul>
    <li>Completion of 28 hours of range and classroom training by Class "K" firearms instructor(s).</li>
    <li>Classroom training options: in-person or live online instruction with verification of identity and attendance.</li>
    <li>8 hours of in-person range training covering safe handling and storage of firearms.</li>
    <li>The qualification allows using specific revolver or semiautomatic handgun calibers for regulated duties.</li>
    <li>Licensees can only carry firearms they have completed training with.</li>
    <li>Submission of the original white page from Certificate of Firearms Proficiency for Statewide Firearms License with the license application.</li>
</ul>

<h2>Annual Firearms Requalification Training and Reporting Requirement</h2>
<ul>
    <li>Submission of the original white page from the Certificate of Firearms Proficiency for Statewide Firearms License annually before the expiration date.</li>
</ul>

<h2>Firearms Instruction</h2>
<ul>
    <li>Use of Firearms Training Manual Student Handbook and Study Guide (FDACS-P-02079).</li>
    <li>Use of Firearms Training Manual Instructor's Guide (FDACS-P-02078) by Class "K" licensed firearms instructors.</li>
    <li>Firearms instructors may use audio/video material as instructional aid but not rely solely on it.</li>
    <li>Classroom training must be conducted in person or through live online instruction with verification of identity, attendance, and completion.</li>
    <li>Generation of Certificates of Firearms Proficiency for Statewide Firearms License by Class "K" instructors.</li>
    <li>Provision of electronic or paper copies of certificates to Class "G" students within 3 business days.</li>
    <li>Retention of completed certificates by instructors.</li>
</ul>

<h2>Firearms Instructor Recordkeeping Requirements</h2>
<ul>
    <li>Maintenance of daily schedule, class logs (in-person and online), digital logs for online students' attendance and identity verification method, copies of issued certificates, and student test records.</li>
    <li>Original paper records or scanned electronic records can meet the requirements.</li>
    <li>Accessibility of records to department investigators upon request.</li>
    <li>Retention of records for a minimum of two years at the instructor's place of business or address on the instructor's license.</li>
    <li>Production of student records for inspection upon request.</li>
</ul>

<h2>Firearm Online Training</h2>
<ul>
    <li>Completion of up to 20 hours of classroom training through live online instruction.</li>
    <li>Class "K" instructors must have a physical location in Florida and conduct online instruction from there.</li>
    <li>Live access to online courses for auditing, monitoring, or inspection by department investigators.</li>
    <li>Use a secure website with SSL or TLS technology for online instruction and testing.</li>
    <li>Verification of student identity using U.S. state or federal-issued photo identification.</li>
    <li>Documentation of student attendance in digital log.</li>
    <li>Limitation of student log-in to a single device.</li>
    <li>Inclusion of security questions for active student participation.</li>
    <li>The minimum time requirement for screen reading and discussion.</li>
    <li>Opportunity for students to submit questions to the instructor.</li>
    <li>Option to deliver missed class hours through recorded instruction with question submission.</li>
    <li>Administration of online tests with randomized questions.</li>
</ul>

<h2>Instructor Requirements for Online Courses</h2>
<ul>
    <li>Maintenance of records for all training sessions, including instructor name, license number, and proof of compliance with security protocols at the instructor's place of business.</li>
    <li>Immediate accessibility of records to department investigators upon request.</li>
    <li>Explanation to investigators if electronic records are not immediately available, with the provision of records within 3 business days.</li>
</ul>

<h2>Firearms Online Training</h2>
<ul>
    <li>Completing all or part of the 20 hours of classroom training through live online instruction.</li>
    <li>Live transmission format with Class "K" instructors and students participating simultaneously.</li>
    <li>Class "K" instructors offering online training must maintain a physical location in Florida.</li>
    <li>Online instruction is conducted from a physical location in Florida.</li>
    <li>Live access is provided to department investigators for auditing, monitoring, or inspection upon request.</li>
    <li>Online instruction and testing are conducted through a secure website using SSL or TLS technology.</li>
    <li>Student identity verification using U.S. state or federal-issued photo identification (e.g., driver's license, state ID card).</li>
    <li>Verify and document students' daily attendance in a digital log.</li>
    <li>Utilize an online platform that always restricts student log-in to a single device.</li>
    <li>Inclusion of security questions (challenges) to ensure active student participation, with successful responses required for each question.</li>
    <li>At least one security question is provided every two hours of instruction.</li>
    <li>Opportunity for re-attempting an unsuccessful response within 5 minutes.</li>
    <li>Failure to respond successfully is marked as student absence.</li>
    <li>Class "K" instructor(s) may allow student explanations for unsuccessful responses, making up for lost time, or failure for non-participation.</li>
    <li>Minimum time required for reading screens with text:</li>
    <ul>
        <li>One minute per every 50 words of text.</li>
        <li>Proration for screens with fewer than 50 words.</li>
        <li>Discussion of content on each screen by the instructor.</li>
    </ul>
    <li>Students were allowed to submit questions to Class "K" instructors regarding the classroom training.</li>
    <li>Delivery of up to 4 hours of missed class through recorded instruction for absent students.</li>
    <li>Students can submit questions to Class "K" instructors regarding the recorded instruction.</li>
    <li>Online tests are administered with randomized questions.</li>
</ul>
